
// Write a program to print # sign 5 times

package looping_constructs.using_while;

public class Design1 {

    public static void main(String[] args){

        // declaration

        // input

        // processing

        // start point

        int r = 1;
        while (r <= 3){

            int counter = 1;

            // check point
            // move forward till 5
            while (counter <= 5){

                // print a # character
                System.out.print("# ");

                // change point
                // increment by 1 step
                counter++;
            }

            System.out.println();

            r++;
        }



        // output


    }

}
