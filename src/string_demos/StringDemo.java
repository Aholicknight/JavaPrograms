package string_demos;

public class StringDemo {
    public static void main(String[] args) {

        String text1 = "Hello Everybody !!";
        String text2 = "Hello World !!";

        System.out.println("Text1 : " + text1);
        System.out.println("Text2 : " + text2);

        // display a particular character from string
        System.out.println("At 0: " + text1.charAt(0));
        System.out.println("At 1: " + text1.charAt(1));
        System.out.println("At 2: " + text1.charAt(2));
        System.out.println("At 3: " + text1.charAt(3));
        System.out.println("At 4: " + text1.charAt(4));

    }
}
